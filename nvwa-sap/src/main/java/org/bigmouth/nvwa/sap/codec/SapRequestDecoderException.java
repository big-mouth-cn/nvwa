package org.bigmouth.nvwa.sap.codec;

public class SapRequestDecoderException extends SapDecoderException {

	private static final long serialVersionUID = 1L;

	public SapRequestDecoderException() {
		super();
	}

	public SapRequestDecoderException(String message, Throwable cause) {
		super(message, cause);
	}

	public SapRequestDecoderException(String message) {
		super(message);
	}

	public SapRequestDecoderException(Throwable cause) {
		super(cause);
	}
}
